namespace LeetCode.Tests.SolutionTests;

public class ValidParenthesesUnitTest
{
    [Theory]
    [InlineData("()", true)]
    [InlineData("()[]{}", true)]
    [InlineData("(]", false)]
    [InlineData("([)]", false)]
    [InlineData("{[]}", true)]
    public void MultipleDataTest(string s, bool expected)
    {
        var actual = Solution.IsValid(s);
        Assert.Equal(expected, actual);
    }
}